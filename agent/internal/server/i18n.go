type uiLang string

const (
	uiLangEN uiLang = "en"
	uiLangZH uiLang = "zh"
)

func normalizeUILang(raw string) uiLang {
	raw = strings.ToLower(strings.TrimSpace(raw))
	if strings.HasPrefix(raw, "zh") || raw == "cn" {
		return uiLangZH
	}
	return uiLangEN
}

func (lang uiLang) t(key string) string {
	if value := uiTranslations[lang][key]; value != "" {
		return value
	}
	if value := uiTranslations[uiLangEN][key]; value != "" {
		return value
	}
	return key
}

func jsI18N(lang uiLang) string {
	data, err := json.Marshal(uiTranslations[lang])
	if err != nil {
		data = []byte("{}")
	}
	return "const i18n = " + string(data) + ";\n"
}

